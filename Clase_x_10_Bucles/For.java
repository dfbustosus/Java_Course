package Clase_x_10_Bucles;

public class For {
    public static void main (String args[]){
        // Cuando i ==10 no se cumple la condicion y para
        for (int i=0; i<10;i++){
            System.out.println(i);
        }
        System.out.println("--------------");
        // Incrementos diferentes de 1
        for (int i=0; i<8;i+=4){
            System.out.println(i);
        }

    }
    
}
